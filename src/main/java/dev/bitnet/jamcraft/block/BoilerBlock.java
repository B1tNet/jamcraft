package dev.bitnet.jamcraft.block;

import dev.bitnet.jamcraft.tile.BoilerTileEntity;
import net.minecraft.block.Block;
import net.minecraft.block.BlockState;
import net.minecraft.block.material.Material;
import net.minecraft.block.material.MaterialColor;
import net.minecraft.state.BooleanProperty;
import net.minecraft.state.StateContainer;
import net.minecraft.tileentity.TileEntity;
import net.minecraft.util.math.BlockPos;
import net.minecraft.util.math.shapes.ISelectionContext;
import net.minecraft.util.math.shapes.VoxelShape;
import net.minecraft.world.IBlockReader;
import net.minecraftforge.common.ToolType;

import javax.annotation.Nullable;

public class BoilerBlock extends Block {
    public static final BooleanProperty HEATED = BooleanProperty.create("heated");

    public static final VoxelShape SHAPE = Block.makeCuboidShape(0,1,0,16,24, 16);

    public BoilerBlock() {
        super(Properties.create(Material.IRON, MaterialColor.GRAY)
                .harvestTool(ToolType.PICKAXE)
                .hardnessAndResistance(5.0f,6.0f)
        );


        this.setDefaultState(this.getStateContainer().getBaseState().with(HEATED, false));
    }

    @Override
    public boolean isTransparent(BlockState state) {
        return true;
    }

    @Override
    public VoxelShape getShape(BlockState state, IBlockReader worldIn, BlockPos pos, ISelectionContext context) {
        return SHAPE;
    }

    @Override
    protected void fillStateContainer(StateContainer.Builder<Block, BlockState> builder) {
        builder.add(HEATED);
    }

    @Override
    public boolean hasTileEntity(BlockState state) {
        return true;
    }

    @Nullable
    @Override
    public TileEntity createTileEntity(BlockState state, IBlockReader world) {
        return new BoilerTileEntity();
    }
}
